import * as EASING from '../src/Easing';

describe('Animation::Easing', () => {
  it('Should properly export easing', () => {
    expect(EASING.inQuad(100, 1000, 20, 1000)).toBeCloseTo(1000.2);
    expect(EASING.outQuad(100, 1000, 20, 1000)).toBeCloseTo(1003.8);
    expect(EASING.inOutQuad(100, 1000, 20, 1000)).toBeCloseTo(1000.4);
    expect(EASING.inOutQuad(900, 1000, 20, 1000)).toBeCloseTo(1019.6);
    expect(EASING.inCubic(100, 1000, 20, 1000)).toBeCloseTo(1000.02);
    expect(EASING.outCubic(100, 1000, 20, 1000)).toBeCloseTo(1005.42);
    expect(EASING.inOutCubic(100, 1000, 20, 1000)).toBeCloseTo(1000.08);
    expect(EASING.inOutCubic(900, 1000, 20, 1000)).toBeCloseTo(1019.92);
    expect(EASING.inQuart(100, 1000, 20, 1000)).toBeCloseTo(1000.002);
    expect(EASING.outQuart(100, 1000, 20, 1000)).toBeCloseTo(1006.878);
    expect(EASING.inOutQuart(100, 1000, 20, 1000)).toBeCloseTo(1000.016);
    expect(EASING.inOutQuart(900, 1000, 20, 1000)).toBeCloseTo(1019.984);
    expect(EASING.inQuint(100, 1000, 20, 1000)).toBeCloseTo(1000.0002);
    expect(EASING.outQuint(100, 1000, 20, 1000)).toBeCloseTo(1008.1902);
    expect(EASING.inOutQuint(100, 1000, 20, 1000)).toBeCloseTo(1000.0032);
    expect(EASING.inOutQuint(900, 1000, 20, 1000)).toBeCloseTo(1019.9968);
    expect(EASING.inSine(100, 1000, 20, 1000)).toBeCloseTo(1000.246);
    expect(EASING.outSine(100, 1000, 20, 1000)).toBeCloseTo(1003.128);
    expect(EASING.inOutSine(100, 1000, 20, 1000)).toBeCloseTo(1000.489);
    expect(EASING.inExpo(100, 1000, 20, 1000)).toBeCloseTo(1000.039);
    expect(EASING.inExpo(0, 1000, 20, 1000)).toBeCloseTo(1000);
    expect(EASING.outExpo(100, 1000, 20, 1000)).toBeCloseTo(1010);
    expect(EASING.outExpo(1000, 1000, 20, 1000)).toBeCloseTo(1020);
    expect(EASING.inOutExpo(0, 1000, 20, 1000)).toBeCloseTo(1000);
    expect(EASING.inOutExpo(1000, 1000, 20, 1000)).toBeCloseTo(1020);
    expect(EASING.inOutExpo(100, 1000, 20, 1000)).toBeCloseTo(1000.039);
    expect(EASING.inOutExpo(900, 1000, 20, 1000)).toBeCloseTo(1019.960);
    expect(EASING.inCirc(100, 1000, 20, 1000)).toBeCloseTo(1000.100);
    expect(EASING.outCirc(100, 1000, 20, 1000)).toBeCloseTo(1008.717);
    expect(EASING.inOutCirc(100, 1000, 20, 1000)).toBeCloseTo(1000.202);
    expect(EASING.inOutCirc(900, 1000, 20, 1000)).toBeCloseTo(1019.797);
    expect(EASING.inElastic(1000, 1000, 20, 1000)).toBeCloseTo(1020);
    expect(EASING.inElastic(0, 1000, 20, 1000)).toBeCloseTo(1000);
    expect(EASING.inElastic(100, 1000, -20, 1000)).toBeCloseTo(999.960);
    expect(EASING.inElastic(100, 1000, 20, 1000)).toBeCloseTo(1000.039);
    expect(EASING.outElastic(0, 1000, 20, 1000)).toBeCloseTo(1000);
    expect(EASING.outElastic(1000, 1000, 20, 1000)).toBeCloseTo(1020);
    expect(EASING.outElastic(100, 1000, -20, 1000)).toBeCloseTo(975);
    expect(EASING.outElastic(100, 1000, 20, 1000)).toBeCloseTo(1025);
    expect(EASING.inOutElastic(0, 1000, 20, 1000)).toBeCloseTo(1000);
    expect(EASING.inOutElastic(1000, 1000, 20, 1000)).toBeCloseTo(1020);
    expect(EASING.inOutElastic(100, 1000, -20, 1000)).toBeCloseTo(999.993);
    expect(EASING.inOutElastic(100, 1000, 20, 1000)).toBeCloseTo(1000.006);
    expect(EASING.inOutElastic(900, 1000, 20, 1000)).toBeCloseTo(1019.993);
    expect(EASING.inBack(100, 1000, 20, 1000)).toBeCloseTo(999.713);
    expect(EASING.outBack(100, 1000, 20, 1000)).toBeCloseTo(1008.176);
    expect(EASING.inOutBack(100, 1000, 20, 1000)).toBeCloseTo(999.249);
    expect(EASING.inOutBack(900, 1000, 20, 1000)).toBeCloseTo(1020.750);
    expect(EASING.inBounce(100, 1000, 20, 1000)).toBeCloseTo(1000.237);
    expect(EASING.outBounce(100, 1000, 20, 1000)).toBeCloseTo(1001.512);
    expect(EASING.outBounce(300, 1000, 20, 1000)).toBeCloseTo(1013.612);
    expect(EASING.outBounce(600, 1000, 20, 1000)).toBeCloseTo(1015.45);
    expect(EASING.outBounce(990, 1000, 20, 1000)).toBeCloseTo(1019.877);
    expect(EASING.inOutBounce(100, 1000, 20, 1000)).toBeCloseTo(1000.6);
    expect(EASING.inOutBounce(900, 1000, 20, 1000)).toBeCloseTo(1019.4);
  });
});
